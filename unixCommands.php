<h1>Unix Commands</h1>
<p><strong>pwd </strong>- (present working directory) shows you where you are in the directory</p>
<p><strong>ls </strong>- list files in directory (ls -al shows hidden and system files)</p>
<p><strong>cd </strong>- goes to directory</p>
<p><strong>cd www-logs/ </strong>- takes you to logs</p>
<p><strong>head </strong>- shows like the first ten lines of a file</p>
<p><strong>tail </strong>- shows the last few lines of a file</p>
<p><strong>cat </strong>- shows the entire file</p>
<p><strong>cd www-root/cs148develop</strong></p>
<p><strong>vim filename</strong>- (.user.ini)to get into vim</p>
<p><strong>display_errors=1</strong></p>
<p><strong>escape, colon, w, q </strong>- this is how you save, escape colon to get out, w to write, q to quit</p>
<p><strong>vim .htaccess </strong>- this is how you create a file with vim</p>
<p><strong>rm filename</strong> - removes a file</p>
